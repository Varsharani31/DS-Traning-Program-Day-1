# WAP for slicing of string

name = "Varsharani"
print(name[0])    # Output: V
print(name[1])    # Output: a
print(name[-1])    # Output: i
# print(name[15])    # Output: IndexError: string index out of range
print(name[0:5])  # Output: Varsh
print(name[1:])   # Output: arsharani
print(name[:6])   # Output: Varsha
print(name[:])   # Output: Varsharani 
print(name[1:8:2])   # Output: asaa
print(name[::-1])  # Output: inarahsrav
