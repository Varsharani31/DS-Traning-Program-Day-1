# Simple Interest

p_amt = int(input("Enter Principal Amount : "))
rot = int(input("Enter Rate of Interest : "))
time = int(input("Enter the duration of loan amount : "))

si = p_amt*rot*time/100

print("Simple Interest : ",si)

# Output

# Enter Principal Amount : 100000
# Enter Rate of Interest : 10
# Enter the duration of loan amount : 1
# Simple Interest :  10000.0
